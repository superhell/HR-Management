    private void loadTable() {
        try {

            if (checkInList != null && checkInList.size() > 0) {
                Object[][] data = new Object[checkInList.size()][6];

                for (int i = 0; i < checkInList.size(); i++) {
                    CheckInEntity checkIn = checkInList.get(i);
                    Calendar checkInTime = fromXMLGregorianCalendar(checkIn.getCheckInTime());
                    SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
                    data[i][0] = sdf.format(checkInTime.getTime());
                }

                Object[] columnNames = new Object[1];

                columnNames[0] = "Check In Time";

                TableModel tableModel = new DefaultTableModel(data, columnNames);
                jTableAttendance.setModel(tableModel);

            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "An unknown error has occurred: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

