    private static java.util.List<sessionbean.CheckInEntity> getCheckInList(java.lang.String arg0) {
        sessionbean.CheckInBeanService service = new sessionbean.CheckInBeanService();
        sessionbean.CheckInBean port = service.getCheckInBeanPort();
        return port.getCheckInList(arg0);
    }

    private static Calendar fromXMLGregorianCalendar(XMLGregorianCalendar xc)
            throws DatatypeConfigurationException {
        Calendar c = Calendar.getInstance();
        c.setTimeInMillis(xc.toGregorianCalendar().getTimeInMillis());
        return c;
    }
